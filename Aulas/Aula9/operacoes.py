# Metodos

def soma(a,b):
    soma = a + b
    return soma

def sub(a, b):
    subtracao = a - b
    return subtracao

def mult(a, b):
    mult = a * b
    return mult

def div(a, b):
    div = a // b
    return div

def div_frac(a, b):
    div_frac = a / b 
    return div_frac

def resto(a, b):
    resto = a % b
    return resto

def raiz(a, b):
    raiz = a**(1/b)
    return raiz