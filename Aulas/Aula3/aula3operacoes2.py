# Declaração de variaveis inteiro

n1 = 10
n2 = 3

resultado1 = n1 / n2 # Divisão, retorna o resultado
resultado2 = n1 // n2 # Divisão, retorna o resultado inteiro
resultado3 = n1 % n2 # Divisão, retorna o resto
print(resultado1)
print(resultado2)
print(resultado3)

resultado4 = 3**3 # Multiplicação, exponeciação
resultado5 = 9**0.5 # Multiplicação, raiz quadrada
print(resultado4)
print(resultado5)