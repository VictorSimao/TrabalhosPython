# Estrutura if.. else
"""
if <condicao>:
    codigo
elif <condicao>:
    codigo
else:
    codigo
"""

# Variavel booleana
ativo = bool() # Declarar sem especificar é False
print(ativo)

# Criação de variavel booleana através de expressão
idade = 18
validador = (idade == 18)
print(validador)

validador = (idade != 18)
print(validador)